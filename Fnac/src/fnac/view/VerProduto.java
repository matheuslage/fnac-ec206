    public void atualizarProdutoList() {
        produtoListModel.clear();
        
        List<Produto> filmes = produtoDAO.listar();
        for (Produto filme : filmes) {
            produtoListModel.addElement(filme);
        }
        
        produtoList.setModel(produtoListModel);
    }
    
    public void atualizarProdutoList(List<Produto> filmes) {
        produtoListModel.clear();
        
        for (Produto filme : filmes) {
            produtoListModel.addElement(filme);
        }
        
        produtoList.setModel(produtoListModel);
    }

