    public void atualizarFuncionarioList() {
        funcionarioListModel.clear();

        List<Funcionario> clientes = funcionarioDAO.listar();
        for (Funcionario cliente : clientes) {
            funcionarioListModel.addElement(cliente);
        }

        funcionarioList.setModel(funcionarioListModel);
    }

    public void atualizarClienteList(List<Funcionario> clientes) {
        funcionarioListModel.clear();

        for (Funcionario cliente : clientes) {
            funcionarioListModel.addElement(cliente);
        }

        funcionarioList.setModel(funcionarioListModel);
    }

