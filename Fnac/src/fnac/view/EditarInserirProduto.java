    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed
        String nome = nomeTextField.getText();
        String marca = marcaTextField.getText();
        int estoque = 0;
        double preco = 0;
        if(!(nome.length() > 1)) {
            validacaoLabel.setText("Insira um nome v√°lido");
            return;
        }
        if(!(marca.length() > 1)) {
            validacaoLabel.setText("Insira uma marca v√°lida");
            return;
        }
        try {
            estoque = Integer.parseInt(estoqueTextField.getText());
            if(estoque < 0 || estoque >= 999) {
                throw new NumberFormatException();
            }
        } catch(NumberFormatException e) {
            validacaoLabel.setText("Insira uma valor v√°lido");
            return;
        }
        try {
            preco = Double.parseDouble(precoTextField.getText());
            if(preco < 0 || preco >= 999999) {
                throw new NumberFormatException();
            }
        } catch(NumberFormatException e) {
            validacaoLabel.setText("Insira um pre√ßo v√°lido");
            return;
        }
        
        Produto produto =  new Produto();
        produto.setNome(nome);
        produto.setMarca(marca);
        produto.setEstoque(estoque);
        produto.setPreco(preco);
        boolean resultado;
        if(produtoAtual != null){
            produto.setId(produtoAtual.getId());
            resultado = produtoDAO.atualizar(produto);
        } else {
            resultado = produtoDAO.inserir(produto);
        }
        if(resultado == false) {
            JOptionPane.showMessageDialog(this, "Erro ao editar/inserir produto");
        }
        verProduto.atualizarProdutoList();
        dispose();
    }//GEN-LAST:event_salvarButtonActionPerformed

